//Escreva um programa que verifica se um número é positivo, negativo ou zero.

// Observações:
// Um número positivo é um número maior que 0.

// Saída esperada:
// O número 5 é positivo

let number = -1

if (number > 0) {
    console.log(`O número ${number} é positivo`);
} else if (number < 0) {
    console.log(`O número ${number} é negativo`);
} else {
    console.log("O número é 0");
}